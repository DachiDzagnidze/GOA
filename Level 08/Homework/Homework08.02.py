num = 15

print("----------- AND -----------")

print(num >= 1 and num <= 10) 
print(num >= 13 and num <= 4) 
print(num > 5 and num <= 10) 
print(num > 5 and num > 10)
print(num <= 12 and num ==5) 


print("----------- OR -----------")

print(num >= 2 or num <= 10) 
print(num >= 1 or num <= 4) 
print(num < 4 or num <= 10) 
print(num > 5 or num > 5) 
print(num < 3 or num <10)