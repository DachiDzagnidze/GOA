// მომხმარებლის მიერ ორი რიცხვის შეყვანა
let num1 = parseFloat(prompt("შეიყვანეთ პირველი რიცხვი:")); // პირველი რიცხვი
let num2 = parseFloat(prompt("შეიყვანეთ მეორე რიცხვი:")); // მეორე რიცხვი

// მომხმარებლის მიერ ოპერაციის არჩევა
let operation = prompt("აირჩიეთ ოპერაცია: '+' - მიმატება, '-' - გამოკლება");

// ოპერაციის შესრულება
if (operation === "+") {
    alert("შედეგი: " + (num1 + num2)); // მიმატება
} else if (operation === "-") {
    alert("შედეგი: " + (num1 - num2)); // გამოკლება
} else {
    alert("არასწორი ოპერაცია!"); // შეცდომის მესიჯი
}
