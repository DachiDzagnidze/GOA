
#! 1) .upper(), .lower(), .capitalize() მაგალითები

# .upper() - ყველა სიმბოლოს გადაყავს დიდი ასოებით.
string1 = "GamaRjoBa SaMKarO"
upper_string = string1.upper() 
print(upper_string)

# .lower() - ყველა სიმბოლოს გადაყავს პატარა ასოებით.
string2 = "GamaRjoBa SaMKarO"
lower_string = string2.lower()  
print(lower_string)

# .capitalize() - მხოლოდ პირველ ასო გადაყავს დიდად და დანარჩენებს პატარას ტოვებს.
string3 = "GamaRjoBa SaMKarO"
capitalized_string = string3.capitalize()  
print(capitalized_string)

